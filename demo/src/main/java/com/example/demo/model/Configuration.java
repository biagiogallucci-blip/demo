package com.example.demo.model;

public class Configuration {
	
	private Boolean hasExclusionRules;
	private Boolean hasCustomParameters;
	
	public Boolean getHasExclusionRules() {
		return hasExclusionRules;
	}
	public void setHasExclusionRules(Boolean hasExclusionRules) {
		this.hasExclusionRules = hasExclusionRules;
	}
	public Boolean getHasCustomParameters() {
		return hasCustomParameters;
	}
	public void setHasCustomParameters(Boolean hasCustomParameters) {
		this.hasCustomParameters = hasCustomParameters;
	}
}